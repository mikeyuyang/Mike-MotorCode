/* HD request frame define */
typedef struct tagHD1_REQ_FRAME
{
  UINT8		m_Head[2];
  UINT8		m_Len;
  UINT8		m_FunCode;
  UINT8		m_Data[7];
  UINT8		m_Resv[2];
  UINT8		m_ChkSum;
}HDY_REQ_FRAME;

/* HD answer format define */
typedef struct tagHD1_ANS_FRAME
{
	UINT8	  m_Head[2];
	UINT8	  m_FunCode;
	UINT8	  m_Address;
	UINT8	  m_Len;
	UINT8	  m_Data[7];
	UINT8	  m_CRC16[2];
}HDY_ANS_FRAME;

/* HD Active delivery format define */
typedef struct tagHD1_ACT_FRAME
{
	UINT8	  m_Head[2];
	UINT8	  m_FunCode;
	UINT8	  m_Len;
	UINT8	  m_FWPosition[2];
	UINT8	  m_FWSpeed[2];
	UINT8	  m_FYPosition[2];
	UINT8	  m_FYSpeed[2];
	UINT8	  m_Compass[2];
	UINT8	  m_GPSData[4];
	UINT8	  m_GPSflag[2];
	UINT8	  m_MoveFlag[2];
	UINT8	  m_CRC16[2];

}HDY_ACT_FRAME;

